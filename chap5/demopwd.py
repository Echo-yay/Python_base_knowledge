'''从键盘录入密码 ，最多录取三次，正确就不再录入'''

for i in range(3):
    pwd = input("请输入密码：")
    if pwd == 'ASD':
        print("输入成功！正在跳转...")
        break
    else:
        print("密码错误！请重新输入！")
        i = i+1
        if i == 3:
            print("输入次数已达上限，该卡已锁定！")